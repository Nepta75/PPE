	Le groupe automobile BMW emploie les technologies les plus modernes en particulier en recherche, en développement et en production. Fidèles à cette devise, les départements Technique de mesure, Intégration géométrique des usines de Dingolfing et Regensburg en Allemagne se sont dotées du Track ScanArm de FARO, un système de mesure intelligent et mobile. Les deux sites avaient besoin d’un système permettant de relever des points par contact et des surfaces en tous lieux, et de les transférer non pas à l’aide d’un outil informatique, mais par les airs. Le calibrage n’est donc pas réalisé à partir de points de référence puisque ceux-ci sont un facteur d’incertitude, en raison des pièces mobiles notamment. Les usines BMW avaient pour objectif affiché le transfert par les airs sans contact. Le système qu’elles souhaitaient acquérir devait répondre à cette condition première. Les sites de Regensburg et de Dingolfing ont finalement opté pour le Track ScanArm de FARO, qui correspondait au mieux à leur profil d’exigences.
